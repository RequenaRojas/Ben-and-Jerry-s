public class Dirrecion {
    int id_dirrec;
    String calle_dirrec;
    int num_ext_dirrec;
    int num_int_dirrec;
    String alcaldia;
    String colonia;
    int cp_colonia;

    public int getId_dirrec() {
        return id_dirrec;
    }

    public void setId_dirrec(int id_dirrec) {
        this.id_dirrec = id_dirrec;
    }

    public String getCalle_dirrec() {
        return calle_dirrec;
    }

    public void setCalle_dirrec(String calle_dirrec) {
        this.calle_dirrec = calle_dirrec;
    }

    public int getNum_ext_dirrec() {
        return num_ext_dirrec;
    }

    public void setNum_ext_dirrec(int num_ext_dirrec) {
        this.num_ext_dirrec = num_ext_dirrec;
    }

    public int getNum_int_dirrec() {
        return num_int_dirrec;
    }

    public void setNum_int_dirrec(int num_int_dirrec) {
        this.num_int_dirrec = num_int_dirrec;
    }

    public String getAlcaldia() {
        return alcaldia;
    }

    public void setAlcaldia(String alcaldia) {
        this.alcaldia = alcaldia;
    }

    public String getColonia() {
        return colonia;
    }

    public void setColonia(String colonia) {
        this.colonia = colonia;
    }

    public int getCp_colonia() {
        return cp_colonia;
    }

    public void setCp_colonia(int cp_colonia) {
        this.cp_colonia = cp_colonia;
    }
    
    
    
    
}
